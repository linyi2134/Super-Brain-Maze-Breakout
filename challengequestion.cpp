// 根据等级返回棋盘（0简单 1普通 2困难 3专家 4噩梦）
const std::vector<std::vector<std::string>>& ChallengeQuestion::getBoardByLevel(int level) {
    switch (level) {
    case 0: return getSimpleBoard();
    case 1: return getNormalBoard();
    case 2: return getHardBoard();
    case 3: return getExpertBoard();
    case 4: return getNightmareBoard();
    default: return getNormalBoard();
    }
}